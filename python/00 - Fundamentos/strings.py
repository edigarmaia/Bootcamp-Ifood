curso = "pYthon"

# maiusculas
print(curso.upper())

# minusculas
print(curso.lower())

# só inicial maiuscula
print(curso.title())

# remove os espaços em branco
print(curso.strip())

# remove os espaços em branco da esquerda
print(curso.lstrip())
      
# remove os espaços em branco da direita
print(curso.rstrip())   

# centraliza o obejto
print(curso.center(14))
# centraliza o obejto
print(curso.center(14, "#"))

# separa os caracteres com . - junção
print(".".join(curso))


